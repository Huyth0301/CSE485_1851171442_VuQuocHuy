  <p style="text-align: justify;">Là năm thứ hai liên tiếp hoạt động giao lưu được Khoa CNTT và công ty Qualica duy trì để trang bị cho sinh viên những kỹ năng cần thiết ngay khi còn học tập trên giảng đường đại học. Trước đó, Khoa CNTT cũng có nhiều hợp tác với doanh nghiệp nâng cao cơ hội học tập, việc làm cho sinh viên như hợp tác với Công ty công nghệ NashTech - thuộc tập đoàn Harvey Nash Anh Quốc; hợp tác với Fsoft;&nbsp;Samsung…</p>
  
  <p style="text-align: justify;">Với những hoạt động tích cực trong kết nối doanh nghiệp, song song với việc không ngừng nâng cao chất lượng đào tạo, Khoa CNTT đang khẳng định vị thế của mình trong lòng sinh viên cũng như với chính các đối tác tuyển dụng. Khoa hy vọng, các chương trình giao lưu với Qualica sẽ được tổ chức đều đặn và liên tục trong những năm tiếp theo, tạo bước đệm cho những hợp tác sâu hơn giữa hai bên.</p>
  
  
          
