      <p style="text-align: justify;">(TLU) – Chiều ngày 23/04/2018, tại Trường Đại học Thủy lợi diễn ra buổi giao lưu giữa sinh viên Khoa CNTT với các lãnh đạo và nhân viên của công ty Qualica đang làm việc tại Nhật Bản</p>
  
  <p style="text-align: justify;">Thành lập ngày 1/11/1982, Công ty Qualica là một trong những doanh nghiệp của Nhật Bản có nhiều đóng góp cho xã hội bằng cách cung cấp các giải pháp công nghệ chất lượng cao trên các lĩnh vực: Dịch vụ điện toán đám mây; Phát triển phần mềm; Quản lý hệ thống…</p>
  
  <p style="text-align: justify;">Nhằm giúp sinh viên của Khoa có nhiều cơ hội tiếp cận với doanh nghiệp, đặc biệt là các doanh nghiệp nước ngoài, chiều ngày 23/04/2018, tại Trường Đại học Thủy lợi diễn ra buổi giao lưu giữa sinh viên Khoa CNTT với các lãnh đạo của công ty Qualica và khoảng 25 nhân viên đang làm việc tại Nhật Bản. Buổi gặp mặt được diễn ra trong không khí thân mật, cởi mở. Theo TS. Đặng Thị Thu Hiền – Phó Trưởng Khoa CNTT: Với mục tiêu hướng tới sinh viên sau khi tốt nghiệp đáp ứng được yêu cầu của nơi tuyển dụng. Khoa CNTT luôn gắn đào tạo theo nhu cầu của Doanh nghiệp, đưa các em sinh viên đến với Doanh nghiệp càng sớm càng tốt. Chính vì vậy hàng năm Khoa luôn tổ chức những sự kiện cho sinh viên gặp gỡ, tiếp xúc, giao lưu trao đổi với các công ty trong và ngoài nước, mục đích để nâng cao nhận thức, hiểu biết về ngành nghề, cơ hội nghề nghiệp, các kỹ năng cần có của một kỹ sư IT.</p>
  
  <p style="text-align: justify;"><img alt="" src="http://cse.tlu.edu.vn/Portals/0/2018/31190107_1567848953324281_9052988749474113028_n.jpg" style="width: 800px; height: 500px;" title=""></p>
  
  <p style="text-align: justify;">Tại đây, sinh viên Khoa CNTT được giao lưu, trò chuyện về các trải nghiệm thú vị và mới lạ về đất nước, văn hóa Việt Nam của các nhân viên Nhật Bản theo hình thức chia nhóm. Đây là cơ hội để sinh viên có thể trực tiếp tham gia thảo luận, nghe giới thiệu về cơ hội việc làm IT, yêu cầu của một nhân viên IT tại Nhật Bản, một số kỹ năng trong công việc, khảo sát và nhiều kỹ năng mềm khác...</p>
  
  <p style="text-align: justify;"><img alt="" src="http://cse.tlu.edu.vn/Portals/0/2018/31290479_1567848909990952_5837829953930989532_n.jpg" style="width: 800px; height: 500px;" title=""></p>
  
