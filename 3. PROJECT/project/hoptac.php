                        <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Vào ngày &nbsp;20/01/2016 tại trường Đại học Thủy Lợi đã diễn ra lễ kí kết hợp tác đào tạo với Công ty Fsoft – FPT.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Tham dự buổi lễ có GS.TS Nguyễn Quang Kim - Bí thư Đảng Ủy, Hiệu trưởng Nhà trường; TS Nguyễn Thanh Tùng -Trưởng khoa CNTT; Ông Trần Xuân Khôi - Phó tổng giám đốc công ty FSOFT cùng các giảng viên và sinh viên khoa Công nghệ thông tin- Trường Đại học Thủy Lợi.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Tại buổi kí kết, &nbsp;GS.TS Nguyễn Quang Kim đại diện Trường Đại học Thủy Lợi giới thiệu về nhà trường với công ty FSOFT:&nbsp; về quy mô, cơ sơ vật chất, các lĩnh vực đào tạo, đồng thời&nbsp; nói lên tầm quan trọng&nbsp; của việc hợp tác và định hướng nghề nghiệp cho các sinh viên. Nhà trường rất vui mừng và ủng hộ, tạo điều kiện cho những chương trình thiết thực như vậy.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;"><img alt="" src="http://cse.tlu.edu.vn/Portals/0/cntt%20ky%20ket%201.jpg?ver=2016-01-21-145202-357"></p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">TS Nguyễn Thanh Tùng - Trưởng khoa Công nghệ thông tin (CNTT) phát biểu: “Việc ký kết hợp tác với các doanh nghiệp sẽ không chỉ đưa doanh nghiệp tham gia vào quá trình đào tạo mà còn đem lại nhiều cơ hội cho các sinh viên khoa CNTT.”&nbsp;</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Tiếp đó, Ông Trần Xuân Khôi - Phó tổng giám đốc công ty FSOFT bày tỏ sự vui mừng khi được tham gia hợp tác với Trường - ngôi trường có bề dày lịch sử, có sự đào tạo chuyên sâu về khối ngành kĩ thuật và xây dựng. Bên cạnh đó, Ông cũng đã giới thiệu về công ty với những lĩnh vực hoạt động nổi trội, rất phù hợp với những ngành nghề CNTT&nbsp; đào tạo ở Đại học Thủy lợi.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Chương trình hợp tác đào tạo giữa Khoa CNTT-ĐHTL và công ty Fsoft nhằm trang bị các kiến thức, các kỹ năng lập trình ứng dụng đáp ứng yêu cầu của nhà tuyển dụng. Đây là chương trình rất hữu ích cho sinh viên, các bạn được trang bị cơ bản và nâng cao về SQL, lập trình ứng dụng với các ngôn ngữ C++/Win32, .Net và Java. Sinh viên sẽ có cơ hội được học tập và thực hành với những kiến thức thực tế nhất do giảng viên của FSOFT đảm nhận. Mỗi khóa đào tạo sẽ kéo dài 320 giờ cho sinh viên năm cuối của khoa CNTT. Sau khóa học này, các bạn sinh viên sẽ được cấp chứng nhận của FSOFT và có nhiều cơ hội được phỏng vấn vào làm việc tại FSOFT. Đây là một cơ hội rất tốt cho sinh viên được thử sức, trải nghiệm và làm quen với một môi trường làm việc hàng đầu và chuyên nghiệp tại Việt Nam.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;"><img alt="" src="http://cse.tlu.edu.vn/Portals/0/cntt%20ky%20ket%202.jpg?ver=2016-01-21-145222-670"></p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Lễ ký kết &nbsp;là mốc son đánh dấu quá trình hợp tác tốt đẹp và lâu dài giữa trường Đại học Thủy Lợi và Công ty FSOFT. Dưới sự chứng kiến của đại diện Ban lãnh đạo Nhà trường, khoa CNTT, các thầy, cô giáo và các bạn Sinh viên, TS Nguyễn Thanh Tùng - Trưởng khoa CNTT và Ông Trần Xuân Khôi - Phó tổng giám đốc công ty FSOFT đã lên kí hợp đồng hợp tác.&nbsp;</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Việc liên kết hợp tác đào tạo, &nbsp;mang đến cho sinh viên trường Đại học Thủy Lợi những nền tảng tốt , mở ra bước ngoặt và là giấy thông hành tạo lợi thế cạnh tranh giúp sinh viên nhà trường ngay sau khi tốt nghiệp dễ dàng ứng tuyển, đảm nhận các vị trí quan trọng tại các doanh nghiệp chuyên về CNTT.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">Thông tin về khóa học:</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Đối tượng: Tất cả các sinh viên K53, K54 và một số sinh viên có thành tích học tập tốt (điểm TB tích lũy &gt;=2.3) của K55-Khoa CNTT.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">&nbsp;2) Quyền lợi:</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Học phí: Sinh viên không phải đóng bất kỳ khoản phí nào liên quan đến đào tạo.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Cơ hội việc làm: Sau khóa học, Fsoft tuyển dụng làm nhân viên thực tập với các sinh viên vượt qua buổi sát hạch (tương lai sẽ làm việc tại Fsoft – tham gia các dự án lớn trong nươc và dự án nước ngoài). Với các sinh viên không muốn làm việc tại Fsoft, các bạn vẫn có cơ hội cao về việc làm đối với doanh nghiệp khác khi được trang bị những kỹ năng lập trình ứng dụng hữu ích trên.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nghĩa vụ: Sinh viên theo học phải làm bản cam kết với Khoa tham gia đầy đủ các buổi học lý thuyết và thực hành.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">3) Tóm tắt về chương trình học:</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Thời gian: Tổng thời gian 40 * 8 = 320 giờ học tại Nhà C5-Trung Tâm Thực Hành trường Đại học Thủy Lợi, có thể bố trí vào buổi tối và thứ 7, chủ nhật.</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Chỉ tiêu: Tuyển sinh 02 lớp, mỗi lớp tối đa 40 sinh viên</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">4)Các môn học:</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">·&nbsp;&nbsp;&nbsp;Know-How: 13 ngày</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quy trình làm việc</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;SQL Cơ bản</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;SQL Nâng cao</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">·&nbsp;&nbsp;&nbsp;Industry Readiness: 27 ngày</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kỹ năng lập trình (Basic and OOP): 12 ngày – Lựa chọn 1 trong 3 ngôn ngữ (C++, Java, .Net).</p>
                    <p style="text-align: justify; color: #000000; margin-bottom: 12px; margin-left: 1px;">-&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Thực hành dự án: 15 ngày</p>
